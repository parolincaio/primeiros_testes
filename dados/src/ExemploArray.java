public class ExemploArray {
    public static void main(String[] args) {
        // Criando array de 5 posições
        int[] numeros = new int[5];

        // Atribuindo valores
        numeros[0] = 10;
        numeros[1] = 20;
        numeros[2] = 30;

        // Acessando valores
        System.out.println(numeros[0]); // 10
        System.out.println(numeros[2]); // 30
    }
}

